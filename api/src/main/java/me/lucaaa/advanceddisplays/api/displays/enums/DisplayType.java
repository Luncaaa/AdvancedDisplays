package me.lucaaa.advanceddisplays.api.displays.enums;

/**
 * The type of the Display entity (what it will display).
 */
public enum DisplayType {
    /**
     * The entity will be a Text Display.
     */
    TEXT,
    /**
     * The entity will be a Block Display.
     */
    BLOCK,
    /**
     * The entity will be an Item Display.
     */
    ITEM
}
